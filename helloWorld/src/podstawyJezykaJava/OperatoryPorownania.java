package podstawyJezykaJava;

public class OperatoryPorownania {
    public static void main(String[] args) {

        boolean zmienna;
        zmienna = 5 == 2;
        zmienna = 5 != 2;
        zmienna = 5 > 2;
        zmienna = 5 < 2;
        zmienna = 5 >= 2;
        zmienna = 5 <= 2;

        System.out.println(zmienna);
    }
}
